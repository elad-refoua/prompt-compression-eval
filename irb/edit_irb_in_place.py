def replace_para_text(para, new_text):
    """Replace all text in a paragraph while keeping the first run's formatting.

    Also removes any w:hyperlink elements (embedded links) that may exist
    as siblings of the runs, to prevent orphaned URLs from showing up."""
    from docx.oxml.ns import qn as _qn

    # Remove any w:hyperlink elements in the paragraph
    p_element = para._element
    for hyperlink in p_element.findall(_qn('w:hyperlink')):
        p_element.remove(hyperlink)

    if not para.runs:
        para.add_run(new_text)
        return
    first_run = para.runs[0]
    first_run.text = new_text
    for run in para.runs[1:]:
        run._element.getparent().remove(run._element)


def delete_paragraph(para):
    """Remove a paragraph from the document by removing its XML element."""
    p = para._element
    p.getparent().remove(p)
    p._p = p._element = None


def find_para_containing(doc, *markers, start_from=0):
    """Find the first paragraph whose text contains ALL given markers.

    Returns (index, paragraph) or (None, None)."""
    for i, p in enumerate(doc.paragraphs):
        if i < start_from:
            continue
        t = p.text
        if all(m in t for m in markers):
            return i, p
    return None, None


