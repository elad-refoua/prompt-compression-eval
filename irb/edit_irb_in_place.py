def replace_para_text(para, new_text):
    """Replace all text in a paragraph while keeping the formatting of the first run."""
    if not para.runs:
        # No runs: create one
        para.add_run(new_text)
        return
    # Keep the first run's formatting, replace its text, delete other runs
    first_run = para.runs[0]
    first_run.text = new_text
    # Delete all subsequent runs
    for run in para.runs[1:]:
        run._element.getparent().remove(run._element)


def replace_para_keep_runs(para, old_to_new):
    """Replace substrings in paragraph text, attempting to preserve run boundaries.

    For simplicity, collapse to single run if a replacement is made.
    """
    full = para.text
    modified = False
    for old, new in old_to_new.items():
        if old in full:
            full = full.replace(old, new)
            modified = True
    if modified:
        replace_para_text(para, full)
    return modified


